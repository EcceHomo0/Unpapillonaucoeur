                Soukhayna (qui signifie "la paix profonde") est ce que l'on peut appeler "une survivante". Quelques années après avoir survécu à un terrible accident, elle fut diagnostiquée bipolaire. Après avoir traversé de douloureuses épreuves et passé des jours et des nuits à tenter de sortir de cet enfermement, elle a fini par retrouver le goût des choses de la vie, en se raccrochant à des phrases qui l'ont portée et rendue plus forte. Les mettre en oeuvre dans son quotidien lui a donné le courage de se mettre en marche pour aller au bout de ses rêves.
