                <br>
                Depuis 2018, Soukhayna Caristan a publié en auto-édition sa biographie: "Un papillon au coeur", un recueil de poèmes: "Espoirs d'amour", un roman: "La direction du vent", et un recueil de petites phrases du quotidien: "La lumière de papillon", publié chez Améthyste Éditions. "Libère ton inspiration" et "L'amour du roi et l'élixir magique", un conte pour enfants paru dernièrement, viennent compléter la collection.
                Soukhayna partage également son énergie positive à travers son association, éponyme de son autobiographie, en mettant en application ses idées au quotidien et en allant à la rencontre de ceux qui en ont le plus besoin.
